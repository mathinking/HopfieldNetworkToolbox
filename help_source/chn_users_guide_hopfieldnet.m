%% What is hopfieldnet?
% |hopfieldnet| is a MATLAB class that has been developed to solve the 
% Continuous Hopfield Network using MATLAB. 
% 
% |hopfieldnet| has been programmed in a similiar way to other Artificial 
% Neural Networks available in Neural Network Toolbox™, using |classdef| 
% to create and design the class.
% 
% |hopfielnet| is a generic class which can be used to solve any kind of
% combinatorial optimization problem. 
%
% A child class from |hopfielnet|, |tsphopfieldnet|, is specially focused 
% in solving the Traveling Salesman Problem using Continuous Hopfield
% Networks. See the examples using |tsphopfieldnet| to learn more.
